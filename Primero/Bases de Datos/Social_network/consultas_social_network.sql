-- USERNAME AND EMAIL OF ALL USERS WHO HAVE POSTED IN THE LAST 7 DAYS
SELECT C.username, C.email, P.*
FROM `posts` AS P 
JOIN chitter_users AS C ON C.user_id = P.user_id
WHERE posted_at IN (CURDATE() AND SUBDATE(CURDATE(), INTERVAL 7 DAY));
